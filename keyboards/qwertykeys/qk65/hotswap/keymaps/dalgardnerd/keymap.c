bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case ALT_MO1:
            if (record->event.pressed) {
                // Activate layer 1 when pressed
                layer_on(1);
            } else {
                // Deactivate layer 1 when released
                layer_off(1);
                // If Alt-Tab was active, release Alt
                if (alt_tab_active) {
                    unregister_code(KC_LALT);
                    alt_tab_active = false;
                }
            }
            return false;

        case KC_TAB:
            // If layer 1 is active and Tab is pressed
            if (IS_LAYER_ON(1) && record->event.pressed) {
                if (!alt_tab_active) {
                    // First Tab press - register Alt
                    register_code(KC_LALT);
                    alt_tab_active = true;
                }
                // Send Tab (Alt is already held)
                tap_code(KC_TAB);
                return false;
            }
            break;
    }
    return true;
}
